/*
Conditional Operator
------------------------------
(conditional_operator) ? expression1 : expression2
-Evaluates to a boolean
    -If the conditional_operator is true, then the value of expression1 is returned
    -If the conditional_operator is false, then the values of expression2 is returned
-Similar to If-Else construct
-Ternary Operator
-Very useful when use inline
-Very easy to abuse
*/